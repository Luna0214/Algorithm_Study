# 제한 조건
# n은 길이 10,000이하인 자연수입니다.

# ----------------------------------------------------------------------------------------------
def solution(n):
    answer = ''
    for i in range(n):
        if i % 2 == 0:
            answer += "수"
        else:
            answer += "박"
